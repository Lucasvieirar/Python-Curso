def somar(x, y):
    s = x + y
    print(f"a soma de {x} e {y} é {s}")

def subtrair(x, y):
    s = x - y
    print(f"a subtração de {x} e {y} é {s}")

def mutiplica(x, y):
    s = x * y
    print(f"a multiplicação de {x} e {y} é {s}")

def dividir(x, y):
    s = x / y
    print(f"a divisão de {x} e {y} é {s}")

