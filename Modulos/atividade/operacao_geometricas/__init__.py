def circulo(r):
    pi = 3.14
    area = pi * (r**2)
    print(f"a area do circulo de raio: {r} é {area}")

def triangulo(b, h):
    area = (b * h) /2
    print(f"a area do traingulo de base: {b} e altura{h} é {area}")

def quadrado(a):
    area = a**2
    print(f"a area do quadrado de area{a} é {area}")

def retangulo(b , h):
    area = b * h
    print(f"a area do retangulo de base: {b} e altura{h} é {area}")